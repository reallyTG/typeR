library(ALA4R)


### Name: ala_cache_filename
### Title: Returns the name of the cache file associated with the given
###   URL. Note that this file may not actually exist, this function just
###   provides the mapping from URL to filename
### Aliases: ala_cache_filename

### ** Examples

ala_cache_filename("http://biocache.ala.org.au/ws/index/fields")




