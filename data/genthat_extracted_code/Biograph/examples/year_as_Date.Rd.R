### Name: year_as_Date
### Title: Converts a vector of dates in decimal years (calendar years and
###   fractions of year) to object of class 'Date'.
### Aliases: year_as_Date

### ** Examples

	# d=1946.167, which is March 1,1946 
	year_as_Date (x=1946.167,format.out='%d-%m-%Y')
     


