library(DDHFm)


### Name: which.min.diff
### Title: Find index where two vectors are closest
### Aliases: which.min.diff
### Keywords: methods

### ** Examples

#
# Make up two vectors
#
a <- c(1,2,3)
vect <- c(3,2,1)
#
# Now see on which index are the two closest in value 
#
which.min.diff(a,vect)
#[1] 2
#
# ie its the second index where both vectors are actually 2



