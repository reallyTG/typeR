library(DSAIDE)


### Name: dsaideapps
### Title: A function that lets you run a specific DSAIDE app without going
###   through the main menu
### Aliases: dsaideapps

### ** Examples

# To see all available apps, run
dsaideapps()
# To start a specific app, call its name, e.g.
## Not run: dsaideapps('IDDynamicsIntro')



