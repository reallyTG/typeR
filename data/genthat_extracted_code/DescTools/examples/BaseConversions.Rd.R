library(DescTools)


### Name: Base Conversions
### Title: Converts Numbers From Binmode, Octmode or Hexmode to Decimal and
###   Vice Versa
### Aliases: BinToDec DecToBin OctToDec DecToOct HexToDec DecToHex
### Keywords: arith

### ** Examples

DecToBin(c(17, 25))
BinToDec(c(101, 11101))

DecToOct(c(17, 25))
OctToDec(c(11, 77))

DecToHex(c(17, 25))
HexToDec(c("FF", "AA", "ABC"))



