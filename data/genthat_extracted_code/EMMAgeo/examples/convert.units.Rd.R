library(EMMAgeo)


### Name: convert.units
### Title: Function to convert between phi and micrometers.
### Aliases: convert.units
### Keywords: EMMA

### ** Examples

## generate phi-values
phi <- -2:5

## convert and show phi to mu
mu  <- convert.units(phi = phi)
mu

## convert and show phi to mu
convert.units(mu = mu)



