### Name: candlestickChart
### Title: Technical analysts - Candlestick chart: show prices for each
###   period as a continuous line. The box is clear if the closing price is
###   higher than the opening price, or filled red if the closing is lower
###   than the opening price.
### Aliases: candlestickChart

### ** Examples

# google <- get.ohlc.yahoo("GOOG",start="2013-07-01",end="2013-08-01"); candlestickChart(google)
# apple <- get.ohlc.google("AAPL",start="2013-07-01",end="2013-08-01"); candlestickChart(apple)



