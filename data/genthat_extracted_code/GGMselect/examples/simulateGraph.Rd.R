library(GGMselect)


### Name: simulateGraph
### Title: Generate sparse Gaussian Graphical Models
### Aliases: simulateGraph
### Keywords: multivariate graphs models datagen

### ** Examples

# simulate a graph
p=30
eta=0.13
Gr <- simulateGraph(p,eta)

# plot the graph
library(network)
par(mfrow=c(1,1))
gV <- network(Gr$G)
plot(gV,jitter=TRUE, usearrows = FALSE, label=1:p,displaylabels=TRUE)


