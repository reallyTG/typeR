### Name: OneWayVarPlot
### Title: Displays a three-panel 'bwplot' of the data by group, of the
###   group means, and of the entire dataset.  This is an approximate
###   visualization of the Mean Square lines from the ANOVA table for a
###   one-way ANOVA model.
### Aliases: OneWayVarPlot
### Keywords: dplot

### ** Examples

data(batch)
OneWayVarPlot(Calcium ~ Batch, data = batch)



