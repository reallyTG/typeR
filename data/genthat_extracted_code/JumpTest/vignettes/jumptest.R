library(JumpTest)

## ------------------------------------------------------------------------
M <- 390*60
m <- 1200
pric <- SV1FJ(M,m,3,.2,.03,.5,0,.125,-.1,-.62)
lp <- c(3,pric[,1])
lr <- diff(lp[seq(1,M*m+1,60)])
logreturn <- matrix(lr,ncol=m)

## ------------------------------------------------------------------------
pmat <- pcombine(logreturn,c('BNS','Amed','Amin'))

## ------------------------------------------------------------------------
res <- ppool(pmat)
pvalue <- res@pvalue
pvalue_adjusted <- res@adjp

