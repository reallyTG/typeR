library(LPS)


### Name: OVL
### Title: Overlap quantification for LPS object
### Aliases: OVL

### ** Examples

  # Full overlap between identical distributions
  OVL(c(0,0), c(1,1))
  
  # Increasing shift
  OVL(c(0,1), c(1,1))
  OVL(c(0,2), c(1,1))
  OVL(c(0,3), c(1,1))
  OVL(c(0,10), c(1,1))



