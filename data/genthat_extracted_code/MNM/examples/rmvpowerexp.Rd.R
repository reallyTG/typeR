library(MNM)


### Name: rmvpowerexp
### Title: Random Samples From a Power Exponential Distributions
### Aliases: rmvpowerexp
### Keywords: distribution multivariate

### ** Examples

X1 <- rmvpowerexp(100,c(0,0,0),Beta = 0.5)
pairs(X1)
X2 <- rmvpowerexp(100,c(0,0,0),Beta = 1)
pairs(X2)
X3 <- rmvpowerexp(100,c(0,0,0),Beta = 10)
pairs(X3)



