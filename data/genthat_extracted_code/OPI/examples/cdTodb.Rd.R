library(OPI)


### Name: cdTodb
### Title: Convert cd/m^2 to dB
### Aliases: cdTodb
### Keywords: misc

### ** Examples

    dB <- cdTodb(10000/pi)  # 0 dB
    dB <- cdTodb(1000/pi)   # 10 dB
    dB <- cdTodb(100/pi)    # 20 dB
    dB <- cdTodb(10/pi)     # 30 dB
    dB <- cdTodb(1/pi)      # 40 dB
    dB <- cdTodb(0.1/pi)    # 50 dB



