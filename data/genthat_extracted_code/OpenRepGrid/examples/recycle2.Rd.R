### Name: recycle2
### Title: variation of recycle that recycles one vector x or y to the
###   length of the longer one
### Aliases: recycle2
### Keywords: internal

### ** Examples

recycle2(1:10, 1:3)
recycle2(1, 1:5)
recycle2(1, 1:5, na.fill=TRUE)
recycle2(1:5, 5:1)    # vectors unchanged



