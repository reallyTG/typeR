library(QCSimulator)


### Name: innerProduct
### Title: Inner product of 2 vectors and computes the angle between
###   vectors
### Aliases: innerProduct

### ** Examples

# Initialze global variables
init()
phi = matrix(c(1/2,sqrt(3)/2),nrow=2,ncol=1)
si = matrix(c(1/sqrt(2),1/sqrt(2)),nrow=2,ncol=1)
innerProduct(phi,si)





