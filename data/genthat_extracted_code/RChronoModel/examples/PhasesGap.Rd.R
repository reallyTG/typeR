### Name: PhasesGap
### Title: Gap or Hiatus between two successive phases (for phases in
###   temporal order constraint)
### Aliases: PhasesGap
### Keywords: gap between two phases temporal order succession of phases

### ** Examples

  data(Phases); attach(Phases)
  PhasesGap(Phase.1.beta, Phase.2.alpha, 0.95)
  PhasesGap(Phase.1.beta, Phase.2.alpha, 0.50)



