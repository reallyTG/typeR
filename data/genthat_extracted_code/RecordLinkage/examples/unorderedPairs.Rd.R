library(RecordLinkage)


### Name: unorderedPairs
### Title: Create Unordered Pairs
### Aliases: unorderedPairs
### Keywords: misc

### ** Examples

  # create unordered pairs of {1,2,3}: {1,2},{1,3} and {2,3}
  unorderedPairs(3)
  # create unordered pairs of {"a","b","c"}: {"a","b"}, {"a","c"},{"b","c"}
  unorderedPairs(c("a","b","c"))



