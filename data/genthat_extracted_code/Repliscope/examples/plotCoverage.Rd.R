### Name: plotCoverage
### Title: A function to scatterplot 'score' column of a BED dataframe
###   plotCoverage function plots values in the ‘score’ column of the
###   supplied bed dataframe as a function of chromosome coordinates. The
###   genome wide median is plotted as a pink line.
### Aliases: plotCoverage
### Keywords: BED bioinformatics genomics scatterplot

### ** Examples

plotCoverage(W303_G2)
plotObject <- plotCoverage(W303_S,plotting=FALSE)



