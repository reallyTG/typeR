### Name: trimRatio
### Title: A function to remove outliers from the "ratio" column of a
###   supplied ratio dataframe trimRatio is applied to the calculated ratio
###   of read counts from a replicating to a non-replicating samples.
### Aliases: trimRatio
### Keywords: bioinformatics genomics replication

### ** Examples

W303 <- trimRatio(W303,0.5,1.5)



