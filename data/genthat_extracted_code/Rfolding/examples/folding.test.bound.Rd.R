library(Rfolding)


### Name: folding.test.bound
### Title: Computes the confidence bound for the significance level p
### Aliases: folding.test.bound

### ** Examples

n = 2000  	# number of observations
d = 2 	  	# 2 dimensional data
p = 0.05  	# we want the bound at the level 0.05 (classical p-value)
q = folding.test.bound(n,d,p)




