library(SimilarityMeasures)


### Name: DistanceCheck
### Title: Check if Two Points Lie Within some Distance in All Dimensions
### Aliases: DistanceCheck

### ** Examples

# Creating two points.
point1 <- c(0, 2, 4, 6)
point2 <- c(0, 1, 2, 3)

# Running the check with distances 1 and 3 in 4 dimensions.
DistanceCheck(point1, point2, 1, 4)
DistanceCheck(point1, point2, 3, 4)



