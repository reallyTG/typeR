library(StratigrapheR)


### Name: every_nth
### Title: Suppresses every n th element of a vector
### Aliases: every_nth

### ** Examples

numvec <- 0:20

every_nth(numvec, 3)

every_nth(numvec, 3, empty = FALSE)

every_nth(numvec, 3, inverse = TRUE)

every_nth(numvec, 3, empty = FALSE, inverse = TRUE)




