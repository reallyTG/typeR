library(TreeSearch)


### Name: NRooted
### Title: Number of rooted/unrooted trees These functions return the
###   number of rooted or unrooted trees consistent with a given pattern of
###   splits.
### Aliases: NRooted NUnrooted1 LnUnrooted1 LnRooted LnUnrooted NUnrooted
###   LnUnrootedMult NUnrootedMult

### ** Examples

  NRooted(10)
  NUnrooted(10)
  LnRooted(10)
  LnUnrooted(10)
  # Number of trees consistent with a character whose states are 00000 11111 222
  NUnrootedMult(c(5,5,3))




