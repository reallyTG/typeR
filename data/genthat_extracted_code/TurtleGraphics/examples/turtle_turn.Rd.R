library(TurtleGraphics)


### Name: turtle_turn
### Title: Turn (Rotate) the Turtle
### Aliases: turtle_left turtle_right turtle_turn

### ** Examples

turtle_init()
turtle_left(30) # equivalent to turtle_turn(30, "left")
turtle_right(40)
turtle_turn(30, sample(c("left", "right"), 1)) # random turn




