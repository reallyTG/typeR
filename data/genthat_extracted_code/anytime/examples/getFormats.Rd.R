library(anytime)


### Name: getFormats
### Title: Functions to retrieve (or set) formats used for parsing dates.
### Aliases: getFormats addFormats

### ** Examples

  getFormats()
  addFormats(c("%d %b %y",      # two-digit date [not recommended], textual month
               "%a %b %d %Y"))  # weekday weeknumber four-digit year



