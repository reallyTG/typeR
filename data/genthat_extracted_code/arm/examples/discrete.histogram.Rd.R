library(arm)


### Name: discrete.histogram
### Title: Histogram for Discrete Distributions
### Aliases: discrete.histogram discrete.hist
### Keywords: dplot

### ** Examples

a <- c(3,4,0,0,5,1,1,1,1,0)
discrete.histogram (a)

x <- c(0,1,3,4,5)
p <- c(.3,.4,.1,.1,.1)
discrete.histogram (x,p)

x <- c(0,1,3,4,5)
y <- c(3,4,1,1,1)
discrete.histogram (x,y)



