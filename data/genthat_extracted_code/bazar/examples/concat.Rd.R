library(bazar)


### Name: concat
### Title: String concatenation
### Aliases: concat concat0 concat_

### ** Examples

v <- c("Florence", "Julie", "Angela")
concat0(v)
concat_(v)
concat(v, sep = "^^")
concat0(c("a", "b"), c(1, NA, 3), NA)
concat(c(NA, NA))
concat(c(NA, NA), na.rm = FALSE) # usually not desirable




