### Name: monthLabs
### Title: Nicely spaced labels along a month
### Aliases: monthLabs
### Keywords: chron

### ** Examples


monthLabs(2014,2014, 3) # 3 days per month
monthLabs(2013,2014,  npy=3) # 3 months per year, equally spaced
monthLabs(2014,2014,  npy=4) # 4 months per year

