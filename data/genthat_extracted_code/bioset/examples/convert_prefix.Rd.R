library(bioset)


### Name: convert_prefix
### Title: Convert between metric prefixes.
### Aliases: convert_prefix

### ** Examples

convert_prefix(x = 2, from = "ng", to = "kg")
convert_prefix(x = 2, from = "dm^3", to = "cm^3")
convert_prefix(x = 2, from = "fl", to = "pl")
convert_prefix(x = 2, from = "pmol", to = "nmol")




