### Name: longestVec
### Title: The function takes in multiple vectors of any length, and
###   returns the one with the longest length. The tieBreaker variable
###   controls if the first or the last of the longest vectors gets
###   returned in case there are multiple
### Aliases: longestVec

### ** Examples

longestVec(1:5, c('a','b'))




