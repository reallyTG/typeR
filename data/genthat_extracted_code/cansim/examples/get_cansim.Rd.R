library(cansim)


### Name: get_cansim
### Title: Retrieves a Statistics Canada data table using CANSIM code or
###   NDM table number
### Aliases: get_cansim

### ** Examples

# Retrieve a table with an NDM code
get_cansim("34-10-0013")
# Retrieve a table with an old-style CANSIM code
get_cansim("026-0018")




