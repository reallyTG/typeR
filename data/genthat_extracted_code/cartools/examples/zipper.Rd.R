### Name: zipper
### Title: The 'zipper' function can be used to simulate interaction among
###   three vehicles in car following on a single lane or three vehicles
###   merging on a two-lane highway.
### Aliases: zipper

### ** Examples

zipper(0,60,90,90,0,5000,90,90,-200,4500,90,90,-500, 4000)
zipper(0,40,85,90,0,4000,90,99,0,4500,90,90,-500, 4200)
zipper(0,5,60,20,0,500,65,20,-100,467,80,20,-350,433)



