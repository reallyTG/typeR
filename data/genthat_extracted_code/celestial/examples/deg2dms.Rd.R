### Name: deg2dms
### Title: Convert decimal degrees to dms format.
### Aliases: deg2dms
### Keywords: convert

### ** Examples

print(deg2dms(12.345))
print(deg2dms(12.345,type='cat',sep=':'))
print(deg2dms(12.345,type='cat',sep='dms'))
print(deg2dms(12.345,type='cat',sep='DMS'))



