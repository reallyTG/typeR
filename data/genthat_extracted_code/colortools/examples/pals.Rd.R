library(colortools)


### Name: pals
### Title: Palette colors included in colortools
### Aliases: pals

### ** Examples

# default use
pals()

# color names of palette 'terra'
pals("terra")

# pizza wheel for palette 'cheer'
pizza(pals("cheer"), init.angle = 90)

# color wheel for first color in palette 'fish'
wheel(pals("fish")[1])



