library(compoisson)


### Name: com.logspace
### Title: Operations in Log-space
### Aliases: com.log.difference com.log.sum com.log.factorial
### Keywords: manip

### ** Examples

	a = exp(com.log.difference(log(100), log(20))); # a = 80
	b = exp(com.log.sum(log(100), log(20))); # b = 120
	c = exp(com.log.factorial(4)); # c = 24



