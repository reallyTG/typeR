### Name: jointCOP
### Title: Compute Equal Marginal Probabilities Given a Single Joint AND or
###   OR Probability for a Copula
### Aliases: jointCOP
### Keywords: joint probability

### ** Examples

jointCOP(0.50, cop=GHcop, para=1.5, type="and") # 0.6461941  0.6461941  0.5000000
jointCOP(2/3,  cop=GHcop, para=1.5, type="or" ) # 0.4994036  0.4994036  0.6666667

