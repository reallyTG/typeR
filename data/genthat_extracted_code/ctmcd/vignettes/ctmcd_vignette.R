library(knitr)

## ------------------------------------------------------------------------
library(ctmcd)

## ------------------------------------------------------------------------
data(tm_abs)

## ------------------------------------------------------------------------
tm_rel=rbind((tm_abs/rowSums(tm_abs))[1:7,],c(rep(0,7),1))
gmda=gm(tm=tm_rel,te=1,method="DA")
gmda

## ------------------------------------------------------------------------
gm0=matrix(1,8,8)
diag(gm0)=0
diag(gm0)=-rowSums(gm0)
gm0[8,]=0

gmem=gm(tm_abs,te=1,method="EM",gmguess=gm0)
gmem

## ------------------------------------------------------------------------
ciem=gmci(gmem,alpha=0.05)

## ------------------------------------------------------------------------
plot(gmem)

