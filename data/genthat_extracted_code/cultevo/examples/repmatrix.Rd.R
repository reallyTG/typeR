### Name: repmatrix
### Title: Extend a matrix by repetition of elements.
### Aliases: repmatrix

### ** Examples

repmatrix(diag(4))
repmatrix(diag(4), times=2)
repmatrix(diag(4), each=2)
repmatrix(diag(3), times=2, each=2)
repmatrix(diag(4), each.row=2)
repmatrix(diag(4), times.row=2)



