library(cwhmisc)


### Name: numer
### Title: Number theoretic functions
### Aliases: EulerPhi gcd Euclid scm Inv modexp
### Keywords: character,arith

### ** Examples

 scm(35,133) # 665
 gcd(35,133) # 7
 Euclid(35,133) #  -1 4 7, meaning 4*35 +(-1)*133 = 7
 EulerPhi(60) # 16
 modexp(3,10,7)  # 3^10 mod 7: 4



