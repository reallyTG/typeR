### Name: kgV
### Title: Calculates the least common multiple of two natural numbers a
###   and b
### Aliases: kgV
### Keywords: math

### ** Examples


kgV(18, 6)          #   18
kgV(38, 105)        # 3990
kgV(36, 9)          #   36
kgV(12, 9)          #   36
kgV(9, 12)          #   36
kgV(-5, 12)         # NA - only integer numbers > 0 allowed
kgV(3, 0)           # NA - only integer numbers > 0 allowed
kgV(3.2, 12)        # NA - only integer numbers > 0 allowed




