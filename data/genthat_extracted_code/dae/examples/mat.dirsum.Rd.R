### Name: mat.dirsum
### Title: Forms the direct sum of a list of matrices
### Aliases: mat.dirsum
### Keywords: array

### ** Examples

       m1 <- matrix(1:4, nrow=2)
       m2 <- matrix(11:16, nrow=3)
       m3 <- diag(1, nrow=2, ncol=2)
       dsum <- mat.dirsum(list(m1, m2, m3))



