library(dagR)


### Name: anglePoint
### Title: Calculate coordinates at specific angle and distance.
### Aliases: anglePoint

### ** Examples

round(anglePoint(c(0,0),      0, 0.5), 2);
round(anglePoint(c(0,0),   pi/2, 0.5), 2);
round(anglePoint(c(0,0),     pi, 0.5), 2);
round(anglePoint(c(0,0), 1.5*pi, 0.5), 2);



