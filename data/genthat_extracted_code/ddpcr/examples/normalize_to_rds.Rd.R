library(ddpcr)


### Name: normalize_to_rds
### Title: Normalize a file name to .rds suffix
### Aliases: normalize_to_rds
### Keywords: internal

### ** Examples

normalize_to_rds("somefile")       # somefile.rds
normalize_to_rds("somefile.rds")   # somefile.rds
normalize_to_rds("somefile.r")     # somefile.r.rds



