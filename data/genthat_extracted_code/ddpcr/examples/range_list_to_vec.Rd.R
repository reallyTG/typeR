### Name: range_list_to_vec
### Title: Convert a list of ranges to a vector of its individual
###   components
### Aliases: range_list_to_vec
### Keywords: internal

### ** Examples

range_list_to_vec("A01")
range_list_to_vec("A01:A04")
range_list_to_vec("A01, B03")
range_list_to_vec("A01, B02:C04, C07")



