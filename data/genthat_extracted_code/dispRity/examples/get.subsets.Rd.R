library(dispRity)


### Name: get.subsets
### Title: Extracts subsets from a dispRity object.
### Aliases: get.subsets get.dispRity, get.subsets.dispRity

### ** Examples

## Load the disparity data based on Beck & Lee 2014
data(disparity)

## Get one subset
get.subsets(disparity, "60")

## Get two subsets
get.subsets(disparity, c(1,5))




