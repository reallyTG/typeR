### Name: dseries
### Title: Completes multiple series of simulated drop tests.
### Aliases: dseries

### ** Examples

  dseries(num.series = 5, q = 0.05)
  dseries(num.series = 5, q = 0.05, max.trials = 60)
  dseries(num.series = 5, q = 0.05, max.trials = 60, fail.criteria = 2)
  



