library(echarts4r)


### Name: e_lines_3d
### Title: Lines 3D
### Aliases: e_lines_3d e_line_3d e_lines_3d_ e_line_3d_

### ** Examples

# get data
flights <- read.csv(
  paste0("https://raw.githubusercontent.com/plotly/datasets/",
         "master/2011_february_aa_flight_paths.csv")
)

# Lines 3D
# Globe
flights %>% 
  e_charts() %>% 
  e_globe(
    base.texture = e_map_texture(),
    height.texture = e_map_texture(),
    environment = e_stars_texture(),
    displacementScale = 0.05
  ) %>% 
  e_lines_3d(
    start_lon, 
    start_lat, 
    end_lon, 
    end_lat,
    name = "flights",
    effect = list(show = TRUE)
  ) %>% 
  e_legend(FALSE)

# Geo 3D
flights %>% 
  e_charts() %>% 
  e_geo_3d() %>% 
  e_lines_3d(
    start_lon, 
    start_lat, 
    end_lon, 
    end_lat,
    coord.system = "geo3D"
  )
 
# line 3D 
df <- data.frame(
  x = 1:100,
  y = runif(100, 10, 25),
  z = rnorm(100, 100, 50)
)

df %>% 
  e_charts(x) %>% 
  e_line_3d(y, z) %>% 
  e_visual_map() %>% 
  e_title("nonsense")




