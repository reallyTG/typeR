library(emuR)


### Name: rad
### Title: Function to convert between Hertz and Radians
### Aliases: rad
### Keywords: math

### ** Examples


# 4000 Hz in radians at a sampling frequency of 8000 Hz
rad(4000, 8000)
# pi/2 and pi/4  radians in Hz at a sampling frequency of 10000 Hz
rad(c(pi/2, pi/4), 10000, FALSE)





