### Name: SinglePairPL
### Title: Computes correct latent trawl SINGLE pairwise likelihood
###   depending on the values of '(x1,x2)' with exponential trawl function.
### Aliases: SinglePairPL

### ** Examples

SinglePairPL(x1=0.5, x2= 0.3, alpha=0.3, beta=2, kappa=3, B1=0.3, B2=0.7, B3=0.3, FALSE)
SinglePairPL(x1=2.0, x2=1.0, alpha=-2, beta=3., kappa=3,  B1=0.3, B2=0.7, B3=0.3, TRUE)




