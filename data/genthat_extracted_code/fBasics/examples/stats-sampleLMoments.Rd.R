### Name: sampleRobMoments
### Title: Robust Moments for the GLD
### Aliases: sampleRobMoments sampleMED sampleIQR sampleSKEW sampleKURT
### Keywords: distribution

### ** Examples
   
## Sample:
   x = rt(100, 4)
   
## sampleMED -
   # Median:
   sampleMED(x)
 
## sampleIQR - 
   # Inter-quartile Range:
   sampleIQR(x)
 
## sampleSKEW -  
   # Robust Skewness:
   sampleSKEW(x)
   
## sampleKURT -
   # Robust Kurtosis:
   sampleKURT(x)



