### Name: two.indicators
### Title: Data set on attachment dependency (Cook, 2000) The classic Cook
###   (2000) dataset consists of measurements on security of attachment
###   within families. Only the variable measuring attachment dependency in
###   family relationships is included in this dataset. Four roles are
###   present (i.e. two parents and two children): mothers "m", fathers
###   "f", the older child as "c", and the younger child "y".
### Aliases: two.indicators
### Keywords: datasets

