library(future)


### Name: multisession
### Title: Create a multisession future whose value will be resolved
###   asynchronously in a parallel R session
### Aliases: multisession

### ** Examples

## No test: 

## Use multisession futures
plan(multisession)

## A global variable
a <- 0

## Create multicore future (explicitly)
f <- future({
  b <- 3
  c <- 2
  a * b * c
})

## A multisession future is evaluated in a separate R session.
## Changing the value of a global variable will not affect
## the result of the future.
a <- 7
print(a)

v <- value(f)
print(v)
stopifnot(v == 0)

## End(No test)



