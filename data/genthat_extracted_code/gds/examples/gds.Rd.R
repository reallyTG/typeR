library(gds)


### Name: gds
### Title: Descriptive statistics of grouped data: with the help of this
###   package we calculate mean, median, mode, variance, standard
###   deviation, coefficient of variance, quartiles, IQR, skewness, and
###   kurtosis of grouped data.
### Aliases: gds

### ** Examples

gds(c(10,20,30,40,50),c(20,30,40,50,60),c(7,13,23,20,8))





