library(ggasym)


### Name: factor_is_greater
### Title: Determines if the level of a is greater than that of b
### Aliases: factor_is_greater

### ** Examples

first <- c("J", "O", "S", "H")
last <- c("C", "O", "O", "K")
first <- factor(first, LETTERS)
last <- factor(last, LETTERS)
factor_is_greater(first, last)




