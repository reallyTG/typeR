library(ggplot2)


### Name: limits
### Title: Generate correct scale type for specified limits
### Aliases: limits
### Keywords: internal

### ** Examples

ggplot2:::limits(c(1, 5), "x")
ggplot2:::limits(c(5, 1), "x")
ggplot2:::limits(c("A", "b", "c"), "x")
ggplot2:::limits(c("A", "b", "c"), "fill")
ggplot2:::limits(as.Date(c("2008-01-01", "2009-01-01")), "x")



