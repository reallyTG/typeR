### Name: SI_tg_format
### Title: Formatter for tagged labels using SI multipliers
### Aliases: SI_tg_format SI_tagged

### ** Examples

SI_tg_format()(1:10)
SI_tg_format()(runif(10))
SI_tg_format(exponent = 2)(runif(10))
SI_tagged(1:10)
SI_tagged(runif(10))
SI_tagged(runif(10), digits = 2)




