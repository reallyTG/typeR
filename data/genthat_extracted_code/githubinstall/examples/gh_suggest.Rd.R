### Name: gh_suggest
### Title: Suggest Github Repository from a Incomplete Name
### Aliases: gh_suggest

### ** Examples

gh_suggest("AnomalyDetection")
# [1] "twitter/AnomalyDetection"
gh_suggest("BnomalyDetection")
# [1] "twitter/AnomalyDetection"
