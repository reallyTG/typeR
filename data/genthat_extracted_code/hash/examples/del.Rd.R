library(hash)


### Name: del
### Title: Remove key-value pair(s) from a hash
### Aliases: del del-methods del,ANY,hash-method delete delete-methods
###   delete,ANY,hash-method
### Keywords: methods data manip

### ** Examples


  h <- hash( letters, 1:26 )
  h # 26 elements
  del( "a", h )
  h # 25 elements




