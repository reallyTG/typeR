### Name: infoMessages
### Title: Turn on or off information messages.
### Aliases: infoMessages

### ** Examples

# To turn on the info messages (all the same):
infoMessages("on")
infoMessages(1)
infoMessages(TRUE)
# To turn off the info messages (all the same):
infoMessages("off")
infoMessages(0)
infoMessages(FALSE)
# checking output:
(out <- infoMessages(FALSE))



