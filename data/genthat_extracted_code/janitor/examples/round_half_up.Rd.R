### Name: round_half_up
### Title: Round a numeric vector; halves will be rounded up, ala Microsoft
###   Excel.
### Aliases: round_half_up

### ** Examples

round_half_up(12.5)
round_half_up(1.125, 2)
round_half_up(1.125, 1)
round_half_up(-0.5, 0) # negatives get rounded away from zero




