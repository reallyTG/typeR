### Name: daily2fraction
### Title: Converter from daily Date to fraction
### Aliases: daily2fraction
### Keywords: utility

### ** Examples

  daily2fraction(as.Date("2017-01-15")) # 2017.038
  daily2fraction(as.Date("2017-02-14")) # 2017.122
  daily2fraction(as.Date("2017-07-15")) # 2017.538




