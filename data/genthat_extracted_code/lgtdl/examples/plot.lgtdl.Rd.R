library(lgtdl)


### Name: plot.lgtdl
### Title: A function for plotting lgtdl objects.
### Aliases: plot.lgtdl
### Keywords: hplot

### ** Examples

  x1<-data.frame(time=c(1,3,5), cov=c(4,6,8))
  x2<-data.frame(time=c(11,13,15), interest=c(66,45,88))

  x1<-as.lgtdl(x1)
  x2<-as.lgtdl(x2)
  plot(x1)
  plot(x2)




