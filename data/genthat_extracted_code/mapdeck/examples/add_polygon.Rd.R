library(mapdeck)


### Name: add_polygon
### Title: Add Polygon
### Aliases: add_polygon

### ** Examples

## No test: 

## You need a valid access token from Mapbox
key <- 'abc'

df <- melbourne
df$elevation <- sample(100:5000, size = nrow(df))
df$info <- paste0("<b>SA2 - </b><br>",df$SA2_NAME)

mapdeck(
  token = key
  , style = mapdeck_style('dark')
  , location = c(145, -38)
  , zoom = 8
  ) %>%
  add_polygon(
    data = df
    , polyline = "geometry"
    , layer = "polygon_layer"
    , fill_colour = "fillColor",
    , stroke_colour = "fillColor",
    , elevation = "elevation"
    , stroke_width = 0
    , tooltip = 'info'
  )

