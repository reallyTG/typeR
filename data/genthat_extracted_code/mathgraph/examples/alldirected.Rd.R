library(mathgraph)


### Name: alldirected
### Title: Transform to Directed Graph
### Aliases: alldirected alldirected.mathgraph alldirected.default
### Keywords: math

### ** Examples

my.graph <- mathgraph(~ 1:3 / 2:4) # undirected graph with 3 edges
alldirected(my.graph) # directed graph with 6 arcs




