library(matlab)


### Name: repmat
### Title: MATLAB repmat function
### Aliases: repmat
### Keywords: array

### ** Examples

repmat(1, 3)                       # same as ones(3)
repmat(1, c(3, 3))                 # same thing 
repmat(1, 3, 3)                    # same thing
repmat(1, size(matrix(NA, 3, 3)))  # same thing
repmat(matrix(1:4, 2, 2), 3)



