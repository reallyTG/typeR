library(matrixcalc)


### Name: matrix.power
### Title: Matrix Raised to a Power
### Aliases: matrix.power
### Keywords: math

### ** Examples

A <- matrix( c ( 1, 2, 2, 1 ), nrow=2, byrow=TRUE)
matrix.power( A, -2 )
matrix.power( A, -1 )
matrix.power( A, 0 )
matrix.power( A, 1 )
matrix.power( A, 2 )



