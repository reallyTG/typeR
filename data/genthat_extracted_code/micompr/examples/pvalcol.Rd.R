library(micompr)


### Name: pvalcol
### Title: Associate colors to _p_-values
### Aliases: pvalcol
### Keywords: internal

### ** Examples

micompr:::pvalcol(c(0.06, 0.9, 0.0001, 0.3, 0.2, 0.02),
                  c("darkgreen", "yellow", "red"))
# [1] "darkgreen" "darkgreen" "red"       "darkgreen" "darkgreen" "yellow"




