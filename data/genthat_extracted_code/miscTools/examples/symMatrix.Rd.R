### Name: symMatrix
### Title: Symmetric Matrix
### Aliases: symMatrix
### Keywords: array

### ** Examples

   # fill the lower triangular part by columns
   symMatrix( 1:10, 4 )
   # fill the upper triangular part by columns
   symMatrix( 1:10, 4, upper = TRUE )
   # fill the lower triangular part by rows
   symMatrix( 1:10, 4, byrow = FALSE )



