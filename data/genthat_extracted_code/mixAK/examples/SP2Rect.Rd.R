### Name: SP2Rect
### Title: Conversion of a symmetric matrix stored in a packed format
###   (lower triangle only) into a matrix
### Aliases: SP2Rect
### Keywords: array

### ** Examples

SP2Rect(3, dim=1)
SP2Rect(c(1, 0.5, 2), dim=2)
SP2Rect(c(1, 0.5, 0.25, 2, -0.5, 3), dim=3)



