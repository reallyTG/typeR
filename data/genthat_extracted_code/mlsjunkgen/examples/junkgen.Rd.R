library(mlsjunkgen)


### Name: junkgen
### Title: Generate a single pseudo-random number using the MLS Junk
###   Generator algorithm
### Aliases: junkgen

### ** Examples

# Generate a pseudo-random number with user-specified seeds

w <- 1
x <- 2
y <- 3
z <- 4
junkgen(w = w, x = x, y = y, z = z) # returns "[1] 0.9551644"



