library(msgpack)


### Name: unpackMsg
### Title: Decode msgpack messages.
### Aliases: unpackMsg unpackMsgs unpackOpts

### ** Examples

msg <- as.raw(c(0x82, 0xa7, 0x63, 0x6f, 0x6d, 0x70, 0x61, 0x63, 0x74, 0xc3,
                0xa6, 0x73, 0x63, 0x68, 0x65, 0x6d, 0x61, 0x00))
unpackMsg(msg)
x <- packMsgs(list("one", "two", "three"))
unpackMsgs(x, 2)



