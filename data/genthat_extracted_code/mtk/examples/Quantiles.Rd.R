### Name: Quantiles
### Title: The 'Quantiles' function
### Aliases: Quantiles

### ** Examples

Quantiles(seq(0,1,length=11),"unif",list(min=8,max=10))
Quantiles(seq(0,1,length=11),"unif",list(min=8,max=10),shrink=0.5)
Quantiles(seq(0,1,length=11),"norm",list(mean=0, sd=1),shrink=0.5)



