### Name: Stirling2
### Title: Compute Stirling numbers of the second kind
### Aliases: Stirling2 S2
### Keywords: partitions

### ** Examples

## returns S(6, 3)
Stirling2(6, 3)

## returns S(6,1), S(6,2), ..., S(6,6)
S2(6, 1:6)

## returns S(6,1), S(5, 2), S(4, 3)
S2(6:4, 1:3)



