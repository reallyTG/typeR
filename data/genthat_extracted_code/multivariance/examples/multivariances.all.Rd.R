library(multivariance)


### Name: multivariances.all
### Title: simultaneous computation of total/ 2-/ 3- /... multivariance
### Aliases: multivariances.all

### ** Examples

x = coins(100,k = 3)
multivariances.all(x)
# yields the same as:
multivariance(x)
total.multivariance(x)
m.multivariance(x,m=2)
m.multivariance(x,m=3)





