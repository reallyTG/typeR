library(mvp)


### Name: special
### Title: Various functions to create simple multivariate polynomials
### Aliases: special product homog linear xyz numeric_to_mvp
### Keywords: symbolmath

### ** Examples

product(1:3)        #      a * b^2 * c^3
homog(3)            #      a + b + c
homog(3,2)          #      a^2  + a b + a c + b^2 + b c + c^2
linear(1:3)         #      1*a + 2*b + 3*c
constant(5)         #      5
xyz(5)              #      a*b*c*d*e



