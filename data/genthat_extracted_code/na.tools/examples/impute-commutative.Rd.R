### Name: impute-commutative
### Title: Imputation by Cummutative Functions Impute using replacement
###   values calculated from a univariate, cummuative function.
### Aliases: impute-commutative na.max na.min na.mean na.median na.quantile
###   na.mode na.most_freq

### ** Examples

  na.median( c(1,2,NA_real_,3) )
      
  na.quantile( c(1,2,NA_real_,3), prob=0.4 )

  na.mode( c(1,1,NA,4) )
  na.mode( c(1,1,4,4,NA) ) 




