### Name: similarity
### Title: Calculate the similarity between two vectors of logicals.
### Aliases: similarity

### ** Examples

similarity(c(0,1,1),c(0,0,1),"simple_matching")
similarity(c(0,1,1),c(0,0,1),"jaccard")
similarity(as.logical(c(0,1,1)),as.logical(c(0,0,1)),"tanimoto")




