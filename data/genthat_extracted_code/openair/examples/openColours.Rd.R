library(openair)


### Name: openColours
### Title: openair colours
### Aliases: openColours
### Keywords: methods

### ** Examples


# to return 5 colours from the "jet" scheme:
cols <- openColours("jet", 5)
cols

# to interpolate between named colours e.g. 10 colours from yellow to
#  green to red:
cols <- openColours(c("yellow", "green", "red"), 10)
cols





