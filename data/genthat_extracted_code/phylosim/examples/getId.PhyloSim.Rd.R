library(phylosim)


### Name: getId.PhyloSim
### Title: Get the unique identifier of a PhyloSim object
### Aliases: getId.PhyloSim PhyloSim.getId getId,PhyloSim-method

### ** Examples

	# create a PhyloSim object
	o<-PhyloSim(name="MySim");
	# get id
	getId(o);
	# get id via virtual field
	o$id;
 


