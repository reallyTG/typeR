library(phylosim)


### Name: getName.PhyloSim
### Title: Get the name of a PhyloSim object
### Aliases: getName.PhyloSim PhyloSim.getName getName,PhyloSim-method

### ** Examples

	# create a PhyloSim object
	o<-PhyloSim();
	# set/get name
	setName(o,"MySim");
	getName(o,"MySim");
	# set/get name via virtual field
	o$name<-"George";
	o$name
 


