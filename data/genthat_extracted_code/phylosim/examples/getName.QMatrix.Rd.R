### Name: getName.QMatrix
### Title: Get the name of a QMatrix object
### Aliases: getName.QMatrix QMatrix.getName getName,QMatrix-method

### ** Examples

	# create a QMatrix object
	m<-QMatrix()
	# set/get name
	setName(m,"Susie Q")
	getName(m)
	# set/get name via virtual field
	m$name<-"Q"
	m$name
 


