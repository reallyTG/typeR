library(phylosim)


### Name: is.Alphabet
### Title: Check if an object is an instance of the Alphabet class
### Aliases: is.Alphabet.default is.Alphabet

### ** Examples


	# create an alphabet object
	a<-Alphabet()
	# create a PSRoot object
	o<-PSRoot()
	# check if they are alphabet objects
	is.Alphabet(a)
	is.Alphabet(o)

 


