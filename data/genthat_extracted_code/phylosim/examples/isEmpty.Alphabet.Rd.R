library(phylosim)


### Name: isEmpty.Alphabet
### Title: Check if the symbol set is empty
### Aliases: isEmpty.Alphabet Alphabet.isEmpty isEmpty,Alphabet-method

### ** Examples


	# create an empty alphabet
	a<-Alphabet();
	# check whether it is empty
	isEmpty(a)
	# specify a new symbol set
	a$symbols<-c(0,1)
	isEmpty(a)
 


