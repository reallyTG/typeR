### ** Examples

	# create a geometric FastFieldDeletor
	p<-FastFieldDeletor()
	# set/get the first length parameter
	setLengthParam1(p,0.5)
	getLengthParam1(p)
	# set/get the first length parameter via virtual field
	p$lengthParam1<-0.2
	p$lengthParam1
 


