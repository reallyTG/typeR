library(phylosim)


### Name: setName.Process
### Title: Set the name of a Process object
### Aliases: setName.Process Process.setName setName,Process-method

### ** Examples

	# create object
	p<-Process()
	# get name
	p$name
	# set new name
	getName(p)
	# get name and id
	p$name
	p$id
	# set new name via virtual field
	p$name<-"Procey"
	p$name
 


