### Name: setName.QMatrix
### Title: Set the name of a QMatrix object
### Aliases: setName.QMatrix QMatrix.setName setName,QMatrix-method

### ** Examples

	# create a QMatrix object
	m<-QMatrix()
	# set/get name
	setName(m,"Susie Q")
	getName(m)
	# set/get name via virtual field
	m$name<-"Q"
	m$name
 


