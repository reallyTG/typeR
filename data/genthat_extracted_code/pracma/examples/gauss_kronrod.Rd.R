### Name: gauss_kronrod
### Title: Gauss-Kronrod Quadrature
### Aliases: gauss_kronrod
### Keywords: math

### ** Examples

gauss_kronrod(sin, 0, pi)  #  2.000000000000000 , rel.error: 1.14e-12
gauss_kronrod(exp, 0, 1)   #  1.718281828459045 , rel.error: 0
                           #  1.718281828459045 , i.e. exp(1) - 1



