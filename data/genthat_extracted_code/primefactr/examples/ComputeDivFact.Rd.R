library(primefactr)


### Name: ComputeDivFact
### Title: Compute the ratio of factorials.
### Aliases: ComputeDivFact

### ** Examples

choose(100, 20)
ComputeDivFact(100, c(20, 80))
lchoose(100, 20)
ComputeDivFact(100, c(20, 80), out.log = TRUE)

factorial(100)
ComputeDivFact(100)
lfactorial(100)
ComputeDivFact(100, out.log = TRUE)



