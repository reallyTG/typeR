### Name: ciBA
### Title: Bayesian method of CI estimation with different or same
###   parameteric values for Beta prior distribution
### Aliases: ciBA

### ** Examples

n=5; alp=0.05; a=0.5;b=0.5;
ciBA(n,alp,a,b)
n=5; alp=0.05; a=c(0.5,2,1,1,2,0.5);b=c(0.5,2,1,1,2,0.5)
ciBA(n,alp,a,b)



