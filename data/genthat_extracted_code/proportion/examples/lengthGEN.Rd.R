### Name: lengthGEN
### Title: Calculates the sum of lengths for a specific LL and UL
### Aliases: lengthGEN

### ** Examples

n= 5;
LL=c(0,0.01,0.0734,0.18237,0.3344,0.5492)		#Lower and Upper Limits
UL=c(0.4507,0.6655,0.8176,0.9265,0.9899,1)
hp=seq(0,1,by=0.01)
lengthGEN(n,LL,UL,hp)



