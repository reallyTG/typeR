### Name: truncate_dist
### Title: Truncation function for normal distributions (truncates both
###   mean and variance)
### Aliases: truncate_dist
### Keywords: univar

### ** Examples

truncate_dist(a = -1, b = 3, mean = 0, sd = 1)
truncate_dist(a = 1, b = Inf, mean = 0, sd = 1)
truncate_dist(a = c(-1, 1), b = c(3, Inf), mean = 0, sd = 1)



