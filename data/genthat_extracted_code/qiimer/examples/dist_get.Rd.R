library(qiimer)


### Name: dist_get
### Title: Retrieve distances from a '"dist"' object.
### Aliases: dist_get

### ** Examples

data(relmbeta_dist)
dist_get(relmbeta_dist, "A1", "A2")
dist_get(relmbeta_dist, "A1", c("A2", "A3", "A4", "A5"))
dist_get(relmbeta_dist, c("A1", "A2", "A3"), c("B1", "B2", "B3"))



