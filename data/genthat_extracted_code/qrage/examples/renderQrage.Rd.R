library(qrage)


### Name: renderQrage
### Title: Force Directed Graph Output with the JavaScript
### Aliases: renderQrage

### ** Examples

renderQrage({
		  data(links)
    	data(nodeColor)
    	data(nodeValues)
    	w <- qrage(links,nodeColor=nodeColor,nodeValue=nodeValues,cut=0.1)
    	return(w)
})



