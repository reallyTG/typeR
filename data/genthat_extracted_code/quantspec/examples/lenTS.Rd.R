library(quantspec)


### Name: lenTS
### Title: Validates if 'Y' is of an appropriate type for a time series and
###   returns the length of the time series.
### Aliases: lenTS
### Keywords: Validator-functions

### ** Examples

Y <- lenTS(sp500)
Y <- lenTS(wheatprices)
Y <- lenTS(rnorm(10))
## Not run: Y <- lenTS("Not a valid input")



