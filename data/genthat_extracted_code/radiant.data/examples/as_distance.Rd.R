library(radiant.data)


### Name: as_distance
### Title: Distance in kilometers or miles between two locations based on
###   lat-long Function based on <URL:
###   http://www.movable-type.co.uk/scripts/latlong.html>. Uses the
###   haversine formula
### Aliases: as_distance

### ** Examples

as_distance(32.8245525,-117.0951632, 40.7033127,-73.979681, unit = "km")
as_distance(32.8245525,-117.0951632, 40.7033127,-73.979681, unit = "miles")




