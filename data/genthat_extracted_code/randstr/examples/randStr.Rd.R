library(randstr)


### Name: randStr
### Title: Generate random strings of a dictated size of symbol set and
###   distribution of the lengths of strings. The output is a list of 2
###   items: [[1]]vector of strings, [[2]]vector of the corresponding
###   symbol set
### Aliases: randStr

### ** Examples

randStr(nString = 10, maxLen = 30, minLen = 1, stdevLen = 15, symbolSetSize = 25)



