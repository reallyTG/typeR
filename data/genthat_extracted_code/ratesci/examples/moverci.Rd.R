library(ratesci)


### Name: moverci
### Title: MOVER confidence intervals for comparisons of independent
###   binomial or Poisson rates.
### Aliases: moverci

### ** Examples

  #Binomial RD, MOVER-J method:
  moverci(x1 = 5, n1 = 56, x2 = 0, n2 = 29)

  #Binomial RD, Newcombe method:
  moverci(x1 = 5, n1 = 56, x2 = 0, n2 = 29, type = "wilson")



