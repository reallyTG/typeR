library(rcosmo)


### Name: plot.CMBWindow
### Title: Visualise a 'CMBWindow'
### Aliases: plot.CMBWindow

### ** Examples


win1 <- CMBWindow(theta = c(0,pi/2,pi/2), phi = c(0,0,pi/2))
win2 <- CMBWindow(theta = c(2*pi/3,3*pi/4,3*pi/4, 2*pi/3), phi = c(pi/4,pi/4,pi/3,pi/3))
plot(win1)
plot(win2)




