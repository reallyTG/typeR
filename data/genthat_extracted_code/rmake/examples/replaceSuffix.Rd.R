library(rmake)


### Name: replaceSuffix
### Title: Replace suffix of the given file name with a new extension
###   (suffix)
### Aliases: replaceSuffix

### ** Examples

replaceSuffix('filename.Rmd', '.pdf')          # 'filename.pdf'
replaceSuffix(c('a.x', 'b.y', 'c.z'), '.csv')  # 'a.csv', 'b.csv', 'c.csv'



