### Name: get_multiplicity
### Title: Find the number of times a value k occurs in a partition that is
###   being generated at random by the multiplicity() function. The
###   resulting multiplicity is then passed back to the multiplicity()
###   function along with an updated value of count and an updated
###   dictionary D
### Aliases: get_multiplicity

### ** Examples

get_multiplicity(10, 5, hash(), 3, 2, TRUE, FALSE)



