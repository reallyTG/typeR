library(rriskDistributions)


### Name: useFitdist
### Title: Fitting amount continuous distributions to given univariate
###   data.
### Aliases: useFitdist
### Keywords: others

### ** Examples

x1 <- rgamma(374, 4,0.08)
res1 <- useFitdist(data2fit = x1)
res1

x2 <- rbeta(300, shape1 = 1, shape2 = 2)
res2 <- useFitdist(data2fit = x2)
res2




