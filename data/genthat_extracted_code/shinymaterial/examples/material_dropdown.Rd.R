library(shinymaterial)


### Name: material_dropdown
### Title: Create a shinymaterial dropdown
### Aliases: material_dropdown

### ** Examples

material_dropdown(
  input_id = "example_dropdown",
  label = "Drop down",
  choices = c(
    "Chicken" = "c",
    "Steak" = "s",
    "Fish" = "f"
  ),
  selected = c("c"),
  multiple = FALSE,
  color = "#ef5350"
)



