library(snp.plotter)


### Name: snp.plotter
### Title: SNP/haplotype association p-value and linkage disequilibrium
###   plotter Creates plots of p-values using single SNP and/or haplotype
###   data. Main features of the package include options to display a
###   linkage disequilibrium (LD) plot and the ability to plot multiple set
###   of results simultaneously. Plots can be created using global and/or
###   individual haplotype p-values along with single SNP p-values. Images
###   are created as either PDF/EPS files.
### Aliases: snp.plotter
### Keywords: aplot, hplot

