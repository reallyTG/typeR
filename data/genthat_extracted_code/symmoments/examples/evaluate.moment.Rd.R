library(symmoments)


### Name:  evaluate
### Title: Evaluate a multivariate moment
### Aliases: ' evaluate.moment' ' evaluate'

### ** Examples


evaluate(callmultmoments(c(1,2,3,4)),c(4,2,1,1,3,1,1,2,1,2))
# evaluates the moment at c(1,2,3,4) at the following covariance matrix
#    4 2 1 1
#    2 3 1 1
#    1 1 2 1
#    1 1 1 2




