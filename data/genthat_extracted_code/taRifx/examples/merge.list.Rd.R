### Name: merge.list
### Title: Method to merge two lists Matches names of each list element and
###   combines any sub-elements
### Aliases: merge.list

### ** Examples

x <- list( A=list(p=runif(5)), B=list(q=runif(5)) )
y <- list( A=list(r=runif(5)), C=list(s=runif(5)) )
merge.list(x,y)



