### ** Examples

# generate 4 sets of cell specifications
specs <- make.specs(
	shape=c(0, 0, 0, 2),           #  circles and squares
    cell.fill=c("red","blue","green", "grey40"),
	back.fill="white",
	scale.max=100
	)




