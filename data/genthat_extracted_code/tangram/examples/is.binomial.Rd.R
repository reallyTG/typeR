library(tangram)


### Name: is.binomial
### Title: Determine if a vector is binomial or not
### Aliases: is.binomial

### ** Examples


is.binomial(c(1,2,3))
is.binomial(factor(c("A","B","C")))
is.binomial(factor(c("A","B","B","A")))
is.binomial(factor(c(TRUE, FALSE, TRUE, FALSE)))
is.binomial(c('M', 'F', 'M', 'F'), 10)



