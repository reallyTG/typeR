### Name: pchisqGC
### Title: Graphical Calculator for Chi-Square Probabilities
### Aliases: pchisqGC

### ** Examples

#This gives P(X < 6.8) where X is chisq with 3 degrees of freedom:
pchisqGC(6.8,df=3,region="below")

#This gives P(X >= 6.8), where X is chisq with 3 degrees of freedom
pchisqGC(6.8,df=3,region="above")



