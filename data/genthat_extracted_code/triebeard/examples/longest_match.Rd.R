### Name: longest_match
### Title: Find the longest match in a trie
### Aliases: longest_match

### ** Examples

trie <- trie(keys = c("afford", "affair", "available", "binary", "bind", "blind"),
                   values = c("afford", "affair", "available", "binary", "bind", "blind"))
longest_match(trie, "binder")




