library(triebeard)


### Name: prefix_match
### Title: Find the prefix matches in a trie
### Aliases: prefix_match

### ** Examples

trie <- trie(keys = c("afford", "affair", "available", "binary", "bind", "blind"),
                   values = c("afford", "affair", "available", "binary", "bind", "blind"))
prefix_match(trie, "aff")




