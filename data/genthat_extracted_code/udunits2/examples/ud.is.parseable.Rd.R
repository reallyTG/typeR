library(udunits2)


### Name: ud.is.parseable
### Title: Determine whether a unit string is parseable by the udunits
###   library
### Aliases: ud.is.parseable

### ** Examples

ud.is.parseable("K")             # TRUE
ud.is.parseable("  K  ")         # TRUE
ud.is.parseable("miles")         # TRUE
ud.is.parseable("Not parseable") # FALSE



