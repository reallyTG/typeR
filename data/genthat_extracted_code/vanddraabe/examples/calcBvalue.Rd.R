### Name: calcBvalue
### Title: Calculate B-value
### Aliases: calcBvalue

### ** Examples

  calcBvalue(rmsfValue=0.25)
  # [1] 4.935
  calcBvalue(rmsfValue=0.50)
  # [1] 19.74
  calcBvalue(rmsfValue=0.75)
  # [1] 44.41
  calcBvalue(rmsfValue=1.0)
  # [1] 78.96
  calcBvalue(rmsfValue=1.25)
  # [1] 123.4




