library(wakefield)


### Name: varname
### Title: Add Internal Name to Vector
### Aliases: varname

### ** Examples

varname(1:10, "A")
attributes(varname(1:10, "A"))
sum(varname(1:10, "A"))

varname(LETTERS, "Caps")
attributes(varname(LETTERS, "Caps"))
paste(varname(LETTERS, "Caps"), collapse="")



