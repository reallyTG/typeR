### Name: IsPowerOfTwo
### Title: Decides whether vector elements are integral powers of two
###   (returns NA if not).
### Aliases: IsPowerOfTwo
### Keywords: arith

### ** Examples

#
# Try and see whether 1,2,3 or 4 are powers of two! 
#
IsPowerOfTwo(1:4)
# [1]  0  1 NA  2
#
# Yes, 1,2 and 4 are the 0, 1 and 2nd power of 2. However, 3 is not an
# integral power of two.



