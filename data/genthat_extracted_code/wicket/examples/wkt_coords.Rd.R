library(wicket)


### Name: wkt_coords
### Title: Extract Latitude and Longitude from WKT polygons
### Aliases: wkt_coords

### ** Examples

wkt_coords("POLYGON ((30 10, 40 40, 20 40, 10 20, 30 10))")
# object  ring lng lat
# 1    1 outer  30  10
# 2    1 outer  40  40
# 3    1 outer  20  40
# 4    1 outer  10  20
# 5    1 outer  30  10




