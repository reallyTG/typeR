library(yum)


### Name: flatten_list_of_lists
### Title: Flatten a list of lists to a list of atomic vectors
### Aliases: flatten_list_of_lists

### ** Examples

### First create a list of lists
listOfLists <-
  list(list(list(1:3, 8:5), 7:7), list(1:4, 8:2));
yum::flatten_list_of_lists(listOfLists);



